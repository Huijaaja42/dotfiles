vim.keymap.set("n", "<leader>x", function()
	local f = vim.fn.expand("%")
	if vim.fn.filereadable(f) == 0 then
		print("File is not readable")
		return
	end
	local c = vim.fn.confirm("chmod +x " .. f, "&Yes\n&No")
	if c == 1 then
		os.execute("chmod +x " .. f)
	end
end, { desc = "E[x]executable" })
